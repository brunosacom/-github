<p> Filmes selecionados que apresentarem cópia final em&nbsp;DCP aberto, devem entregá-la em perfeito estado até o dia<strong> 27 de setembro</strong>. <strong class="form_required">Não serão aceitas cópias fechadas para as mostras competitivas da Première Brasil. </strong><br />
