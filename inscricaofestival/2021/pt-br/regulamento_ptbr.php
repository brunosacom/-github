PREMIERE  BRASIL</p>
<p><strong>REGULAMENTO</strong><br />
O&nbsp;<strong>Festival do Rio 2019</strong>, que acontecerá de&nbsp;07 a 17 de novembro, abre inscrições para seleção de filmes da&nbsp;<strong>Première Brasil</strong>.</p>
<p> A&nbsp;<strong>Première Brasil</strong>, parte integrante da programação do&nbsp;<strong>Festival do Rio</strong>, é composta por mostras competitivas e não-competitivas e exibe apenas filmes brasileiros de curta-metragem e de longa-metragem.</p>
<p> Para fins deste regulamento, considera-se filme brasileiro todo aquele que tenha Certificado de Produto Brasileiro (CPB), registrado junto à Agência Nacional do Cinema (Ancine), ou, aquele que em caso de ser objeto de co-produção internacional, a empresa produtora majoritária seja brasileira.<br />
  <br />
  <strong>DAS INSCRIÇÕES:</strong><br />
  1) As inscrições deverão ser feitas online, pelo site&nbsp;<strong>www.festivaldorio.com.br</strong>&nbsp;(Inscrições&nbsp;<strong>Première Brasil</strong>) de 01 a 16 de agosto  de 2019.<br />
  2) Todas as informações preenchidas no formulário devem ser verídicas. Havendo alguma informação em desacordo com o que fora declarado no momento da inscrição, em caso de seleção, o&nbsp;Festival do Rio&nbsp;se reservará o direito de cancelar a participação do filme.<br />
  3) Todos os filmes para seleção devem ser enviados por meio de links (em sites de compartilhamentos de vídeo por streaming. Não serão aceitos links para download), estes devem ser fornecidos (com senha, se houver) no momento da inscrição em boxes específicos para link e senha disponíveis no formulário.</p>
<p> <strong>Da segmentação da&nbsp;<strong>Première Brasil</strong></strong>:<br />
  A<strong>&nbsp;Première Brasil</strong>&nbsp;se dividirá nas seguintes mostras:<br />
  •&nbsp;<strong>Mostra Competitiva Principal Ficção</strong><br />
  •&nbsp;<strong>Mostra Competitiva Principal Documentário</strong><strong></strong><br />
  •&nbsp;<strong>Mostra Competitiva Principal Curta-Metragem</strong><br />
  •&nbsp;<strong>Mostra Competitiva Novos Rumos</strong><br />
  •&nbsp;<strong>Mostra Competitiva Novos Rumos Curta-Metragem </strong><br />
  •&nbsp;<strong>Mostras Especiais Não Competitivas</strong><strong></strong></p>
<p><strong>DAS CONDIÇÕES DE PARTICIPAÇÃO NA&nbsp;MOSTRA COMPETITIVA PRINCIPAL DA PREMIÈRE BRASIL 2019:</strong><br />
  Para participarem da&nbsp;<strong>Mostra Competitiva Principal da Première Brasil&nbsp;</strong>e estarem aptos a concorrer aos prêmios, os filmes deverão atender às seguintes condições:</p>
<p> <strong>Filmes Longas-Metragens de Ficção</strong></p>
<ol>
  <li>não terem sido recusados em edições anteriores do&nbsp;<strong>Festival do Rio</strong>;</li>
  <li>terem sido finalizados em 2019 ou 2018;</li>
  <li>terem duração superior a&nbsp;70 minutos;</li>
  <li>serem inéditos no Rio de Janeiro e não explorados comercialmente por qualquer mídia em todo território brasileiro;</li>
  <li>apresentarem cópia para o processo de seleção (preferencialmente legendada em inglês) <strong>EXCLUSIVAMENTE por meio de sites de compartilhamento de vídeo em streaming</strong>. Cópias enviadas em DVD e por meio de serviços de download <strong>não</strong> serão aceitas.</li>
  <li>se selecionados, apresentarem cópia final em&nbsp;<strong>DCP aberto</strong>;</li>
  <li>filmes falados em outro idioma que não o português devem - obrigatoriamente - entregar cópia legendada em português para exibição.</li>
</ol>
<p><strong>Filmes Longas-Metragens Documentários</strong></p>
<ol>
  <li>não terem sido recusados em edições anteriores do&nbsp;<strong>Festival do Rio</strong>;</li>
  <li>terem sido finalizados em 2019 ou 2018;</li>
  <li>terem duração superior a&nbsp;70 minutos;</li>
  <li>serem inéditos no Rio de Janeiro e não explorados comercialmente por qualquer mídia em todo território brasileiro;</li>
  <li>apresentarem cópia para o processo de seleção (preferencialmente legendada em inglês) <strong>EXCLUSIVAMENTE por meio de sites de compartilhamento de vídeo em streaming</strong>. Cópias enviadas em DVD e por meio de serviços de download <strong>não</strong> serão aceitas.</li>
  <li>caso selecionados, apresentarem cópia final em&nbsp;<strong>DCP aberto</strong>;</li>
  <li>filmes falados em outro idioma que não o português, devem - obrigatoriamente - entregar cópia legendada em português para exibição.</li>
</ol>
<p><strong>Filmes Curtas-Metragens</strong></p>
<ol>
  <li>não terem sido inscritos em edições anteriores do&nbsp;<strong>Festival do Rio</strong>;</li>
  <li>terem sido finalizados em 2019 ou 2018;</li>
  <li>não terem sido explorados comercialmente por qualquer mídia em todo território brasileiro;</li>
  <li>terem duração de no máximo&nbsp;15&nbsp;minutos;</li>
  <li>apresentarem cópia para o processo de seleção (preferencialmente legendada em inglês) <strong>EXCLUSIVAMENTE por meio de sites de compartilhamento de vídeo em streaming</strong>. Cópias enviadas em DVD e por meio de serviços de download <strong>não</strong> serão aceitas.</li>
  <li>caso selecionados, apresentarem cópia final em&nbsp;<strong>DCP aberto&nbsp;;</strong></li>
  <li>filmes falados em outro idioma que não o português, devem - obrigatoriamente - entregar cópia legendada em português para exibição.</li>
</ol>
<p><strong>DAS CONDIÇÕES DE PARTICIPAÇÃO DE FILMES PARA&nbsp;MOSTRAS ESPECIAIS NÃO COMPETITIVAS&nbsp;E&nbsp;MOSTRA COMPETITIVA NOVOS RUMOS</strong>:<br />
  <br />
  a) terem duração superior a&nbsp;60&nbsp;minutos para longas-metragens;<br />
  b) terem duração inferior a&nbsp;30&nbsp;minutos para curtas-metragens;<br />
  c) não terem sido explorados comercialmente por qualquer mídia em todo território brasileiro;<br />
  d) terem sido finalizados em 2019 ou 2018;<br />
  e) apresentarem cópia para o processo de seleção (preferencialmente legendada em inglês) <strong>EXCLUSIVAMENTE por meio de sites de compartilhamento de vídeo em streaming</strong>. Cópias enviadas em DVD e por meio de serviços de download <strong>não</strong> serão aceitas.<br />
  f) caso selecionados, apresentarem cópia final em&nbsp;DCP aberto;<br />
g) filmes falados em outro idioma que não o português, devem - obrigatoriamente - entregar cópia legendada em português para exibição.</p>
<p> <strong>DA PREMIAÇÃO DA MOSTRA PRINCIPAL:</strong><br />
  Os filmes selecionados para a&nbsp;<strong>Mostra Competitiva Principal da Première Brasil&nbsp;</strong>concorrerão ao&nbsp;<strong>Troféu Redentor</strong>&nbsp;nas seguintes categorias:<br />
  <br />
  I. Melhor Longa-Metragem de Ficção;<br />
  II. Melhor Longa-Metragem Documentário;<br />
  III. Melhor Curta-Metragem;<br />
  IV. Melhor Direção de Ficção;<br />
  V. Melhor Direção de Documentário<br />
  VI. Melhor Ator;<br />
  VII. Melhor Atriz;<br />
  VIII. Melhor Atriz Coadjuvante;<br />
  IX. Melhor Ator Coadjuvante;<br />
  X. Melhor Roteiro;<br />
  XI. Melhor Montagem;<br />
  XII. Melhor Fotografia;<br />
  XIII. Prêmio Especial do Júri;<br />
  XIV. Melhor Longa-Metragem de Ficção de Voto Popular;<br />
  XV. Melhor Longa-Metragem Documentário de Voto Popular;<br />
  XVI. Melhor Curta-Metragem de Voto Popular.<br />
  <br />
  A escolha dos premiados da I a XIII categorias supracitadas será feita por um corpo de jurados composto por especialistas na arte cinematográfica (entre brasileiros e estrangeiros), convidados pela direção do&nbsp;<strong>Festival do Rio</strong>.</p>
<p> Não serão aceitos mais de dois indicados para as categorias de VI a IX (inclusive).</p>
<p> A escolha dos premiados nas categorias de voto popular (categorias XIV a XVI) será feita pelo público que tenha assistido aos filmes concorrentes, por meio de voto direto, através de cédulas distribuídas no início das sessões pagas ou gratuitas que estejam dentro da programação de Voto Popular.</p>
<p> Cada espectador receberá apenas uma cédula por filme, mediante apresentação de ingresso para a respectiva sessão.<br />
  Nas sessões pagas, espectadores convidados pela produção do filme, ou seja, aqueles que retirarem o ingresso gratuitamente junto à produção do&nbsp;<strong>Festival do Rio</strong>, não receberão cédula, portanto não tendo direito a voto.</p>
<p> <strong>DA PREMIAÇÃO DA&nbsp;MOSTRA NOVOS RUMOS</strong>:<br />
  Os filmes selecionados para a&nbsp;<strong>Mostra Competitiva Novos Rumos</strong>&nbsp;concorrerão ao&nbsp;<strong>Troféu Redentor</strong>&nbsp;de:<br />
  <br />
  I. Melhor Filme Longa-metragem<br />
  II. Melhor Filme Curta-metragem<br />
  III. Prêmio Especial do Júri</p>
<p> A escolha dos premiados das categorias supracitadas será feita por um corpo de jurados composto por especialistas na arte cinematográfica (entre brasileiros e estrangeiros), convidados pela direção do&nbsp;<strong>Festival do Rio</strong>.</p>
<p> <strong>DA SELEÇÃO:</strong><br />
  A seleção de filmes participantes da&nbsp;<strong>Première Brasil</strong>, em todas as suas mostras, é de exclusiva competência e responsabilidade do Comitê de Seleção e da Direção do&nbsp;<strong>Festival do Rio</strong>.</p>
<p> Para participar da seleção para a&nbsp;<strong>Première Brasil</strong>&nbsp;é obrigatório o preenchimento da ficha de inscrição e ter o filme finalizado antes da divulgação da seleção.</p>
<p> O resultado da seleção da&nbsp;<strong>Première Brasil 2019</strong>&nbsp;será divulgado a partir da primeira semana de setembro, na seguinte ordem: comunicado aos selecionados (não podendo ser difundido em redes sociais, imprensa, ou qualquer outro meio de comunicação e difusão de informação), site do&nbsp;<strong>Festival do Rio</strong>&nbsp;e posteriormente pela imprensa.</p>
