  <br />
Os filmes selecionados para a&nbsp;Mostra Competitiva Principal&nbsp;terão uma sessão oficial para convidados, uma sessão popular e o máximo de 4 (quatro) sessões para público pagante no circuito do&nbsp;<strong>Festival do Rio</strong>.</p>
<p> As datas e horários das exibições das diversas sessões dos filmes participantes serão decididos a partir de critérios definidos, única e exclusivamente, pela direção e equipe de programação do&nbsp;<strong>Festival do Rio</strong>.</p>
